# int, float, str, bool
print(type(True))
print(False)

# 비교 -> bool : >,>=,<,<=,==,!=
print(5>3)
print(type(5>=3))
print(5<3)
print(type(5<=3))
print(5==3)
print(5!=3)

# 부정
print(not True)
print(not 5>3)

# 파이썬에서 논리연산을 사용하면 0은 거짓, 0이 아니면 참
print(5)
print(not 5)
print(not 0)
print(0==True)
# 파이썬에서 논리연산을 사용하면 ''은 거짓 
print(not '')
print(not 'hello')

